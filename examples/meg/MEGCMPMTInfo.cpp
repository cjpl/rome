Int_t MEGCMPMTInfo::GetFace()
{
   if (Address[0]=='T'&&Address[1]!='C') {
      return TOP;
   }
   if (Address[0]=='B'&&Address[1]=='T') {
      return BOTTOM;
   }
   if (Address[0]=='F') {
      return FRONT;
   }
   if (Address[0]=='B'&&Address[1]=='K') {
      return BACK;
   }
   if (Address[0]=='R') {
      return RIGHT;
   }
   if (Address[0]=='L'&&Address[1]!='S') {
      return LEFT;
   }
   return NOFACE;
}

